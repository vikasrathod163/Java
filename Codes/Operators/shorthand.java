public class shorthand {
    public static void main(String[] args) {
        int a = 20;

        a +=10; //a = a + 10;
        System.out.println(a);

        a-=5;
        System.out.println(a);

        a*=2; //a = a * 2  // a = 25 * 2;
        System.out.println(a);

        int b = 24;

        b/=2;
        System.out.println(b);

    }
    
}
