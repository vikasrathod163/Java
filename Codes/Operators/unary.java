public class unary {
    public static void main(String[] args) {
        System.out.println("Unary Operator : ");

        int a = 50;
        a = -a;
        System.out.println(a);
        a = +a;
        System.out.println(a);
        a = -a;
        System.out.println(a);

        System.out.println();

        System.out.println(a--);

        System.out.println(a++);

        System.out.println(a);

        System.out.println();


        a++;
        System.out.println(a);
        a--;
        System.out.println(a);

        
        System.out.println();

        System.out.println(--a);
        System.out.println(++a);

        System.out.println();



    }
}
