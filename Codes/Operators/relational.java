
public class relational {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a+" == "+b+" : "+(a==b));
        System.out.println(a+" != "+b+" : "+(a!=b));
        System.out.println(a+" < "+b+" : "+(a<b));
        System.out.println(a+" > "+b+" : "+(a>b));
        System.out.println(a+" <= "+b+" : "+(a<=b));
        System.out.println(a+" >= "+b+" : "+(a>=b));

        int a1 = 10;
        int b1 = 10;

        System.out.println();

        System.out.println(a1+" == "+b1+" : "+(a1==b1));
        System.out.println(a1+" != "+b1+" : "+(a1!=b1));
        System.out.println(a1+" < "+b1+" : "+(a1<b1));
        System.out.println(a1+" > "+b1+" : "+(a1>b1));
        System.out.println(a1+" <= "+b1+" : "+(a1<=b1));
        System.out.println(a1+" >= "+b1+" : "+(a1>=b1));

    }
}